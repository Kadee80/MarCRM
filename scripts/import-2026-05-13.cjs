async function main() {
  console.log(`\n📥 Importing ${LEADS.length} leads from daily scrape 2026-05-13\n`);

  let created = 0;
  let skipped = 0;
  let errors = 0;

  for (const lead of LEADS) {
    try {
      const existing = await prisma.company.findFirst({
        where: { name: lead.company.name },
      });

      if (existing) {
        console.log(`⏭️   SKIP (exists): ${lead.company.name}`);
        skipped++;
        continue;
      }

      const company = await prisma.company.create({
        data: lead.company,
      });

      for (const contact of lead.contacts) {
        await prisma.contact.create({
          data: {
            ...contact,
            companyId: company.id,
          },
        });
      }

      await prisma.scrapeResult.create({
        data: {
          companyId: company.id,
          source: lead.company.source,
          summary: `Imported ${lead.company.name} from daily-scrape-2026-05-13. Pipeline: ${lead.company.pipeline}. Combined ${lead.company.fitScore + lead.company.intentScore}.`,
          scrapedAt: new Date("2026-05-13T14:30:00Z"),
        },
      });

      console.log(
        `✅  CREATE: ${lead.company.name}  [${lead.company.pipeline}, combined ${lead.company.fitScore + lead.company.intentScore}]`
      );
      created++;
    } catch (e) {
      console.error(`❌  ERROR: ${lead.company.name} — ${e.message}`);
      errors++;
    }
  }

  console.log(
    `\n✨ Done. Created: ${created} · Skipped (dedup): ${skipped} · Errors: ${errors}\n`
  );

  await prisma.$disconnect();
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
