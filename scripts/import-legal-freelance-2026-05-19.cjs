async function main() {
  console.log(`\n=== Legal Freelance Import — ${SCRAPE_DATE} ===`);
  console.log(`Pipeline: ${PIPELINE}`);
  console.log(`Leads to process: ${LEADS.length}\n`);

  let created = 0;
  let skipped = 0;
  let errors = 0;

  for (const lead of LEADS) {
    const { company, contacts, scrape } = lead;
    try {
      const existing = await prisma.company.findFirst({
        where: { name: company.name },
      });

      if (existing) {
        console.log(`⏭️  SKIP (exists): ${company.name}`);
        skipped += 1;
        continue;
      }

      const createdCompany = await prisma.company.create({
        data: {
          ...company,
          contacts: {
            create: contacts.map((c) => ({
              name: c.name,
              title: c.title,
              linkedin: c.linkedin || null,
              persona: c.persona || null,
              decisionMaker: c.decisionMaker || false,
            })),
          },
          scrapeResults: {
            create: [
              {
                scrapeDate: new Date(SCRAPE_DATE),
                url: scrape.url,
                source: scrape.source,
                matchedSignals: scrape.matchedSignals,
                fitScore: company.fitScore,
                intentScore: company.intentScore,
                pipeline: PIPELINE,
              },
            ],
          },
        },
      });

      console.log(
        `✅ CREATED: ${createdCompany.name} (id=${createdCompany.id}, fit=${company.fitScore}, intent=${company.intentScore})`,
      );
      created += 1;
    } catch (err) {
      console.error(`❌ ERROR on ${company.name}:`, err.message);
      errors += 1;
    }
  }

  console.log(`\n=== Summary ===`);
  console.log(`Created: ${created}`);
  console.log(`Skipped (duplicate): ${skipped}`);
  console.log(`Errors: ${errors}`);

  await prisma.$disconnect();
}

main().catch(async (err) => {
  console.error("Fatal:", err);
  await prisma.$disconnect();
  process.exit(1);
});
