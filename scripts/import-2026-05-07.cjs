// ─── Importer ──────────────────────────────────────────────────────────
async function main() {
  console.log(`\nImporting ${LEADS.length} leads from 2026-05-07 daily scrape...\n`);

  let inserted = 0;
  let skipped = 0;
  const skippedNames = [];

  for (const lead of LEADS) {
    const existing = await prisma.company.findFirst({
      where: { name: lead.company.name },
    });

    if (existing) {
      skipped++;
      skippedNames.push(lead.company.name);
      console.log(`  [skip] ${lead.company.name} (already in DB, id=${existing.id})`);
      continue;
    }

    const company = await prisma.company.create({ data: lead.company });

    for (const c of lead.contacts) {
      await prisma.contact.create({
        data: { ...c, companyId: company.id },
      });
    }

    await prisma.scrapeResult.create({
      data: {
        url: lead.scrape.url,
        source: lead.scrape.source,
        pipeline: lead.company.pipeline,
        resultData: JSON.stringify(lead.company),
        matchedSignals: lead.scrape.matchedSignals,
        imported: true,
      },
    });

    inserted++;
    console.log(`  [add ] ${lead.company.name} (${lead.company.pipeline}, fit=${lead.company.fitScore}, intent=${lead.company.intentScore})`);
  }

  console.log(`\nDone: ${inserted} inserted, ${skipped} skipped.`);
  if (skippedNames.length) {
    console.log(`Skipped (already exists): ${skippedNames.join(", ")}`);
  }
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
