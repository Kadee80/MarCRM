// ─── Importer ──────────────────────────────────────────────────────────
async function main() {
  console.log(`\nImporting ${LEADS.length} leads from 2026-05-12 daily scrape...\n`);

  let inserted = 0;
  let skipped = 0;
  const skippedNames = [];

  for (const lead of LEADS) {
    const { company, contacts, scrape } = lead;

    // Dedup by company name
    const existing = await prisma.company.findFirst({
      where: { name: company.name },
    });
    if (existing) {
      skipped++;
      skippedNames.push(company.name);
      continue;
    }

    const created = await prisma.company.create({
      data: {
        name: company.name,
        website: company.website,
        pipeline: company.pipeline,
        industry: company.industry,
        size: company.size,
        location: company.location,
        fundingStage: company.fundingStage,
        techStack: company.techStack,
        fitScore: company.fitScore,
        intentScore: company.intentScore,
        fitDetails: company.fitDetails,
        intentDetails: company.intentDetails,
        vertical: company.vertical,
        subvertical: company.subvertical,
        engagementModel: company.engagementModel,
        buyerType: company.buyerType,
        compensationText: company.compensationText,
        remoteFlag: company.remoteFlag,
        employmentTypeRaw: company.employmentTypeRaw,
        urgencyScore: company.urgencyScore,
        starred: company.starred,
        notes: company.notes,
        source: company.source,
        lastActivity: company.lastActivity,
        contacts: {
          create: contacts.map((c) => ({
            name: c.name,
            title: c.title,
            persona: c.persona,
            decisionMaker: c.decisionMaker,
          })),
        },
        scrapeResults: {
          create: [
            {
              url: scrape.url,
              source: scrape.source,
              matchedSignals: scrape.matchedSignals,
              scrapedAt: new Date(),
            },
          ],
        },
      },
    });

    console.log(`  + ${created.name}  (${company.pipeline}, fit ${company.fitScore} / intent ${company.intentScore})`);
    inserted++;
  }

  console.log(`\n  Inserted: ${inserted}`);
  console.log(`  Skipped (already exist): ${skipped}`);
  if (skippedNames.length) {
    for (const n of skippedNames) console.log(`    - ${n}`);
  }
  console.log("\nDone.\n");
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
